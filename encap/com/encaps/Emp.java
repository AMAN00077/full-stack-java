package com.encaps;

public class Emp {
	private int empid;
	private String name;
	private String designation;
	
	public void setEmpId(int empid) {
		this.empid=empid;
		
	}
	public int getEmpId() {
		return empid;
	}
	public void  setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	
	public void setDes(String designation) {
		this.designation=designation;
	}
	public String getDes() {
		return designation;
	}

}
